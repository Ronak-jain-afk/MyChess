def mirror_square(sq):
    return (7 - (sq // 8)) * 8 + (sq % 8)

def evaluate(pos: Position) -> int:
    score = 0

    for sq in range(64):
        piece = pos.board[sq]
        if piece == EMPTY:
            continue

        if piece <= 6:
            ptype = PieceType(piece)
            color = Color.WHITE
            score += MATERIAL[ptype]
            score += PST[ptype][sq]
        else:
            ptype = PieceType(piece - 6)
            color = Color.BLACK
            score -= MATERIAL[ptype]
            score -= PST[ptype][mirror_square(sq)]

    return score if pos.side_to_move[0] == 0 else -score